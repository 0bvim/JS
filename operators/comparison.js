const	a = "1";
const	b = 1;

console.log(a == b); //compare if the value is equal to other even if type are different.
console.log(a === b); //compare if type and value are equal.
console.log(a != b); //compare if is not equal.
console.log(a > b); //compare if value is greater than other.
console.log(a < b); //compare if value is less than other.
console.log(a >= b); //compare if value is greater or equal other.
console.log(a <= b); //compare if value is less than or equal to other.
